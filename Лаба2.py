import numpy as np

def max_col_sum(matrix):
    """Находит столбец матрицы, для которого сумма абсолютных значений элементов максимальна"""
    col_sums = np.sum(np.abs(matrix), axis=0)  # находим суммы абсолютных значений элементов столбцов
    max_sum_idx = np.argmax(col_sums)  # находим индекс столбца с максимальной суммой
    return matrix[:, max_sum_idx]  # возвращаем этот столбец

# пример использования
N = 3
M = 4
A = np.random.randint(-10, 10, size=(N, M))  # генерируем матрицу размером N x M со случайными целыми числами от -10 до 10
max_col = max_col_sum(A)
print(f"Исходная матрица:\n{A}")
print(f"Столбец с максимальной суммой абсолютных значений элементов: {max_col}")